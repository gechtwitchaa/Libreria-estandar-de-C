#include <stdio.h>
#include <stdlib.h>

int main() {
    int numero1 = 10;
    int numero2 = -5;

    int absoluto1 = abs(numero1);
    int absoluto2 = abs(numero2);

    printf("El valor absoluto de %d es: %d\n", numero1, absoluto1);
    printf("El valor absoluto de %d es: %d\n", numero2, absoluto2);

    return 0;
}
